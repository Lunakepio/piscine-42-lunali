int	match(int *s1, int *s2)
{
	if (*s1 == '\0')
	{
		if (*s2 == '\0')
			return (0);
		else if (*s2 == '*')
			return (1);
		else
			return (0);
	}
	else
	{
		if (*s2 == '\0')
			return (0);
		else if (*s1 == *s2)
			return (match(s1 + 1, s2 + 1));
		else if (*s2 == '*')
			return (match(s1 + 1, s2) + match(s1, s2 + 1));
	}
	return (0);
}
